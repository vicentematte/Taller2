
public class Habitat {
	private String habitat;

	public Habitat(String habitat) {
		super();
		this.habitat = habitat;
	}

	public String getHabitat() {
		return habitat;
	}

	public void setHabitat(String habitat) {
		this.habitat = habitat;
	}

	@Override
	public String toString() {
		return habitat;
	}
	
	
}
